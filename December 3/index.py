def StarSix():
    start = time.time()
    maxNum = []
    result = 0
    
    for num in fileTwo:
        # How many batteries to skip
        skips_allowed = len(num) - 12
        skips_remaining = skips_allowed
        
        result_string = ""
        position = 0
        
        while len(result_string) < 12:
            # How many more digits do we need?
            needed = 12 - len(result_string)
            
            # How far can we look ahead?
            # We need to ensure we can still collect 'needed' digits
            max_skip_position = len(num) - needed
            window_end = min(position + skips_remaining, max_skip_position) + 1
            
            # Find the best digit we can reach from current position
            look_ahead = num[position : window_end]
            best_digit = max(look_ahead)
            
            # Find the first occurrence of that best digit in our window
            best_position = num.index(best_digit, position, window_end)
            
            # Skip to that position
            skips_used = best_position - position
            skips_remaining -= skips_used
            position = best_position
            
            # Add the best digit to result
            result_string += num[position]
            position += 1
        
        maxNum.append(result_string)
    
    for num in maxNum:
        result += int(num)
    
    end = time.time()
    total_time = end - start
    
    print(f"Total: {result}")
    print(" ")
    print(f"Time taken: {total_time:.4f} seconds\n")
    print(" ")


